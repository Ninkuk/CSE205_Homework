package Assignment7;

public class PhoneCall {
    String number;
    int duration;
    String date;
    String callerName;
    double cost;

    public PhoneCall(String number, int duration, String date, String callerName, double cost) {
        this.number = number;
        this.duration = duration;
        this.date = date;
        this.callerName = callerName;
        this.cost = cost;
    }
}
